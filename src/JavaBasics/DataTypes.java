package JavaBasics;

public class DataTypes {

	public static void main(String[] args) {
		//Integer- positive, negative, zero
		int i = 10;
		int j = 20;
		System.out.println(i);
		System.out.println(i+j);
		System.out.println("the value of i is :"+i);
		System.out.println("the value of i and j  is :"+(i+j));
		
		//double- decimal values
		double d = 12.33d;
		double e = 33.44d;
		System.out.println(d);
		
		//char- only single digits in a single quotes
		char a = '%';
		char b = 'z';
		System.out.println(a);
		System.out.println(a+ " "+b);
		
		//boolean - true and false
		boolean c = true;
		boolean x = false;
		System.out.println(c);
		
		//String- it is a class and not a data type 
		String s = "Hello World";
		String s1 = "Selenium";
		System.out.println(s);
		System.out.println(s+ " "+s1);
		
		//operators < > <= >= == !=
		int e1 = 30;
		int e2 = 40;
		if(e1==e2) {
			System.out.println("e1 is equal to e2");
		}else { System.out.println(" e1 is not equal to e2");
		}
		
		
		
		
		

	}

}
